public class Persona{
    public String nombre;
    public String sexo;
    public int edad;
    public String telefono;
    public String curso;
    public double nota;

    public Persona(String nombre, String sexo, int edad, String telefono, String curso, double nota) {
        this.nombre = nombre;
        this.sexo = sexo;
        this.edad = edad;
        this.telefono = telefono;
        this.curso = curso;
        this.nota = nota;
    }
}
